namespace common\models;


class Singer extends \common\models\gii\Singer
{

    public function behaviors()
    {
        return [
            [
                'class' => 'yii\behaviors\TimestampBehavior',
            ]
        ];
    }

    public function exec() {

        $ret = $this->save();
        return $ret;
    }

    public function getCategory(){
        return $this->hasOne('common\models\Category',  ['id' => 'singer_category_id']);
    }


}